module Y_windowstyle

def self.cmd_window_opacity(window)
    window.opacity = 200		#ウインドウ全体の透明度。0～255で指定
    window.back_opacity = 180		#ウィンドウ背景の透明度。0～255で指定（上記変数より大きい場合自動修正）
    window
  end

end
