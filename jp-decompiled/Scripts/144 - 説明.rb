#~ 　　●通常攻撃
#~  　　装備、職業、アクターのメモ欄に｢<通常攻撃 6>｣と書いて設定します。
#~  　　例の場合、通常攻撃時ID6のスキルを使用します。
#~  　　優先順位は　パッシブスキル　→　武器(右)　→　武器(左)　→　職業　→　アクター　です。
#~  
#~ 　　※通常攻撃設定がVer1.19-β2以降「<通常攻撃 5,8,9>」など複数の項目が設定可能です。
#~ 　　　この場合、通常攻撃時使用するスキルがランダムに選択されることになります。
#~  
#~ 　　●基本能力値
#~  　　アクターのメモ欄に｢<基本能力値 8>｣と書いて設定します。
#~  　　例の場合、アクターはID8の職業のステータスに固定されます。（転職しても変化しない）
#~  
#~ 　　●初期サブクラス
#~  　　アクターのメモ欄に｢<初期サブクラス 2>｣と書いて設定します。
#~  　　例の場合、アクターの初期サブクラスがID2の職業となります。
#~  　　※アクターの開始時の職業と同じにしないでください。

#~  
#~ 　◎ランダムエフェクト関連
#~ 　　●発動スキルの設定
#~  　　スキルのメモ欄に｢<発動スキル 5,10,15,20>｣と書いて設定します。
#~  　　例の場合、このスキルを使用するとID5,10,15,20のいずれかのスキルを選択して使用します。
#~  
#~ 　　●発動アイテムの設定
#~  　　アイテムのメモ欄に｢<発動アイテム 4,7,12,17>｣と書いて設定します。
#~  　　例の場合、このアイテムを使用するとID4,7,12,17のいずれかのアイテムの効果を発揮します。




