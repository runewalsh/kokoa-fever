#~ ②メモ欄の設定
#~ 　◎スティール関連
#~ 　　●スティール能力付与の設定
#~  　　スキルのメモ欄に「<スティール付与 1>」と書いて設定します。
#~  　　例の場合、このスキルはスティール能力1を持ったスキルとして扱われ
#~  　　エディタに設定した能力に加え、スティールリスト1からアイテムを盗むスキルになります。
#~  
#~ 　　●スティールリストの設定
#~  　　エネミーのメモ欄に「<スティールリスト 3,I,5,3>」と書いて設定します。
#~  　　例の場合、このエネミーはスティールリスト3を盗むスキルを使用すると
#~  　　アイテムID5番のアイテムを3分の1の確率で盗めるようになります。
#~  　　この項目は複数個追加することができます。
#~  　　※I=アイテム　A=防具　W=武器　で設定してください。
#~  
#~ 　　●スティール成功率
#~  　　アクター、職業、装備品のメモ欄に「<スティール成功率 120%>」と書いて設定します。
#~  　　例の場合、スティールの成功率が1.2倍になります。
#~  　　複数の設定がある場合、最も高い確率のものが適用されます。


#~ 　◎ドロップ関連
#~ 　　●追加ドロップの設定
#~  　　エネミーのメモ欄に｢<追加ドロップ A,4,30>｣と書いて設定します。
#~  　　例の場合、このエネミーは防具ID4のアイテムを30分の1の確率で落とすようになります。
#~  　　この項目は複数個追加することができます。
#~  　　※１つ目の項目は　I=アイテム　A=防具　W=武器　で設定してください。
#~  
#~ 　　●ドロップ率変化ステート
#~  　　ステートのメモ欄に「<ドロップ変化 200%>」と書いて設定します。
#~  　　例の場合、このステートにかかったエネミーはドロップ率が200%（2倍）になります。
#~  　　複数の増加ステートにかかっている場合、最大の物が適用されます。
#~  　　※このメモ欄設定が設定されたステートは死亡時削除されなくなります。


#~ 　◎スキルの設定
#~ 　　●連続回数
#~  　　スキルのメモ欄に「<連続回数 12>」と書いて設定します。
#~  　　例の場合、スキルの連続回数が12回になります。
#~  
#~ 　　●必要武器タイプ
#~  　　スキルのメモ欄に「<必要武器タイプ 3,4,5>」と書いて設定します。
#~  　　例の場合データベースで設定しているものに加え、ID3,4,5の武器タイプの装備でも
#~  　　スキルが使えるようになります。
#~  
#~ 　　●要求武器タイプ
#~  　　スキルのメモ欄に「<要求武器タイプ 4,9>」と書いて設定します。
#~  　　例の場合、ID4,9の武器タイプを両方装備している時のみスキルが使えるようになります。

#~ 　　●要求装備タイプ
#~  　　スキルのメモ欄に「<要求装備タイプ 1>」と書いて設定します。
#~  　　例の場合、装備タイプ1(デフォルトでは盾)を装備している時のみスキルが使えるようになります。
#~  
#~ 　　●二刀流専用スキル
#~  　　スキルのメモ欄に｢<二刀流要求>｣と書いて設定します。
#~  　　設定されたスキルは二刀流で武器を2本装備している時のみ使えるようになります。
#~  
#~ 　　●同一武器タイプ二刀流専用スキル
#~  　　スキルのメモ欄に「<同一タイプ二刀流専用>」と書いて設定します。
#~  　　設定されたスキルは同一武器タイプの装備を2本装備している時のみ使えるようになります。
#~  
#~ 　　●消費HP
#~  　　スキルのメモ欄に「<消費HP 50>」、「<消費HP 10%>」、「<消費MAXHP 1%>」
#~  　　と書いて設定します。
#~  　　例の場合、スキルを発動するとHPを50、現在HPの10%、最大HPの1%を消費します。
#~  
#~ 　　●消費MP
#~  　　スキルのメモ欄に「<消費MP 20%>」、「<消費MAXMP 5%>」と書いて設定します。
#~  　　例の場合、スキルを発動すると現在MPの20%、最大MPの5%を消費します。
#~  
#~ 　　●消費TP
#~  　　スキルのメモ欄に「<消費TP 15%>」、「<消費MAXTP 5%>」と書いて設定します。
#~  　　例の場合、スキルを発動すると現在TPの15%、最大TPの5%を消費します。
#~  
#~ 　　●消費金額
#~  　　スキルのメモ欄に「<消費金額 400>」と書いて設定します。
#~  　　例の場合、スキルを発動するとゴールドを400消費します。
#~  
#~ 　　●消費アイテム
#~  　　スキルのメモ欄に「<消費アイテム 1-4,5-1>」と書いて設定します。
#~  　　例の場合、スキルを発動するとID1のアイテムを4個、ID5のアイテムを1個消費します。

#~ 　　●獲得アイテム
#~  　　スキルのメモ欄に「<獲得アイテム 5-2,8-1>」と書いて設定します。
#~  　　例の場合、スキルを発動するとID5のアイテムを2個、ID8のアイテムを1個獲得します。
#~  
#~ 　　●消費アイテム（二刀流オプション用）
#~  　　スキルのメモ欄に「<二刀流倍消費>」と書いて設定します。
#~  　　例の場合、同一武器IDで二刀流時、消費アイテムの個数が２倍になります。
#~  
#~ 　　●必要アイテム
#~  　　スキルのメモ欄に「<必要アイテム 4,7,9>」と書いて設定します。
#~  　　例の場合、ID4,7,9のアイテムを所持していなければスキルを使うことができません
#~  
#~ 　　●自爆スキル
#~  　　スキルのメモ欄に｢<自爆スキル>｣と書いて設定します。
#~  　　設定されたスキルを使用後、使用者のHPは0（=戦闘不能）になります。
#~  
#~ 　　●ロストスキル
#~  　　スキルのメモ欄に「<ロストスキル>」と書いて設定します。
#~  　　設定されたスキルは使用後忘れるようになります。
#~  
#~ 　　●仲間想いスキル
#~  　　スキル、アイテムのメモ欄に「<仲間想い 200%>」と書いて設定します。
#~  　　例の場合、味方の戦闘不能者1人ごとに威力が200%上昇します。
#~  
#~ 　　●武器倍率
#~  　　スキル、アイテムのメモ欄に｢<武器倍率 2-30,5-20>｣と書いて設定します。
#~  　　例の場合、ID2,5の武器タイプを装備時、それぞれ30%、20%の追加ダメージが発生します。
#~  
#~ 　　●回復反転無視スキル
#~  　　スキル、アイテムのメモ欄に｢<回復反転無視>｣と書いて設定します。
#~  　　設定されたスキルは｢<回復反転>｣の効果を無視してHPを回復します。　
#~  
#~ 　　●発動スキルの設定
#~  　　スキルのメモ欄に｢<発動スキル 5,10,15,20>｣と書いて設定します。
#~  　　例の場合、このスキルを使用するとID5,10,15,20のいずれかのスキルを選択して使用します。
#~  
#~ 　　●発動アイテムの設定
#~  　　アイテムのメモ欄に｢<発動アイテム 4,7,12,17>｣と書いて設定します。
#~  　　例の場合、このアイテムを使用するとID4,7,12,17のいずれかのアイテムの効果を発揮します。
#~  
#~ 　　●スキルディレイの設定
#~  　　スキルのメモ欄に｢<スキルディレイ 2>｣と書いて設定します。
#~  　　例の場合、設定されたスキルは使用後2ターンは再使用できなくなります。

#~ 　　●拡張スキルタイプ
#~ 　　　スキルのメモ欄に｢<拡張スキルタイプ 3,4>｣と書いて設定します。
#~ 　　　例の場合、設定されたスキルはスキルタイプID3と4のスキル欄にも表示されるようになります。

#~ 　　●連続発動スキル
#~ 　　　スキルのメモ欄に｢<連続発動スキル 1,2,3>｣と書いて設定します。
#~ 　　　例の場合、スキルを使用するとスキルID1、2、3の順にスキルのエフェクトが発生します。

#~   ◎耐久値関連
#~ 　　●耐久値消費
#~  　　スキルのメモ欄に「<耐久値消費 0,50,100%>」と書いて設定します。
#~  　　例の場合、このスキルを使用すると装備ID0（武器）の耐久値が100%の確率で50減少します。
#~  
#~ 　　●耐久値ダメージ
#~  　　スキルのメモ欄に「<耐久値ダメージ 1,10,30%>」と書いて設定します。
#~  　　例の場合、このスキル攻撃を受けると装備ID1（盾）の耐久値が30%の確率で10減少します。

#~ 　　●耐久値回復
#~ 　　 スキル、アイテムのメモ欄に「<耐久値回復 0,40>」と書いて設定します。
#~ 　　 例の場合、このスキルおよびアイテムが使用されると装備ID0（武器）の耐久値が40回復します。

#~ 　◎パッシブスキル関連
#~ 　　●非表示スキル
#~  　　スキルのメモ欄に「<常時非表示>」、「<戦闘中非表示>」、「<メニュー非表示>」
#~  　　のいずれかで設定します。
#~  　　「<常時非表示>」　　　…　スキル使用欄には表示されなくなります。
#~  　　「<戦闘中非表示>」　　…　戦闘中、スキル使用欄に表示されなくなります。
#~  　　「<メニュー非表示>」　…　非戦闘中、スキル使用欄に表示されなくなります。
#~  
#~ 　　●パッシブスキル設定
#~  　　スキル、ステートのメモ欄に「<パッシブ能力武器 3,6>」、「<パッシブ能力防具 2,5>」、「<パッシブ能力職業 7>」
#~  　　と書いて設定します。
#~  　　例の場合設定したスキルを習得している場合　武器ID3と6、防具ID2と5、職業ID7の特徴が
#~  　　アクターの特徴に加わります。
#~  
#~ 　　●発動要求武器タイプ
#~  　　スキルのメモ欄に｢<発動要求武器タイプ 0,3,4>｣と書いて設定します。
#~  　　例の場合、素手及び武器タイプ3か4を装備している状態でなければパッシブスキルが発動しなくなります。
#~  
#~ 　　●発動要求装備タイプ
#~  　　スキルのメモ欄に｢<発動要求装備タイプ 1,2>｣と書いて設定します。
#~  　　例の場合、装備タイプ1（盾）か2（頭）を装備している状態でなければパッシブスキルが発動しなくなります。
#~  

#~ 　◎属性ブースター、属性吸収、装備ブースター、ステートブースター、攻撃属性
#~ 　　●通常攻撃強化
#~  　　アクター、職業、武器、防具、ステートのメモ欄に｢<通常攻撃強化 2-30,4-10>｣と書いて設定します。
#~  　　例の場合、通常攻撃時にID2,4の武器タイプを装備時それぞれ30%、10%の追加ダメージが発生します。
#~  　　武器IDに0を指定すると素手の攻撃力が増加します。
#~  
#~ 　　●属性ブースター
#~  　　アクター、職業、武器、防具、エネミー、ステートのメモ欄に｢<属性強化 3-100,4-50>｣と書いて設定します。
#~  　　例の場合ID3の属性攻撃に100%、ID4の属性攻撃に50%の追加ダメージが発生します。
#~  
#~ 　　（例）敵の属性有効度50%、アクターの属性ブースター50%の場合
#~  　　　　0.5 * 1.5 = 0.75　となり　通常の75%ダメージを与えることができます。
#~  
#~ 　　●武器ブースター
#~  　　アクター、職業、武器、防具、ステートのメモ欄に｢<武器強化物理 1-50,2-100>｣と書いて設定します。
#~  　　例の場合、物理攻撃時にID1,2の武器タイプを装備時それぞれ50%、100%の追加ダメージが発生します。
#~  　　この他に｢<武器強化魔法>｣｢<武器強化必中>｣の設定があり
#~  　　それぞれ魔法攻撃、必中攻撃に対して追加ダメージが発生します。
#~  
#~ 　　●属性吸収
#~  　　アクター、職業、武器、防具、エネミー、ステートのメモ欄に｢<属性吸収 2-50,6-150>｣と書いて設定します。
#~  　　例の場合ID2の属性攻撃の50%、ID6の属性攻撃の150%のダメージを無効化します。
#~  　　無効化した結果、有効度がマイナスになった場合、ダメージを吸収して回復します。
#~  　　
#~  　　（例1）アクターの属性有効度150%、属性吸収100%の場合
#~  　　　　1.5 - 1.0 = 0.5 となり　通常の50%のダメージを受けます。
#~  　　（例2）アクターの属性有効度100%、属性吸収120%の場合
#~  　　　　1.0 - 1.2 = -0.2 となり　通常ダメージの20%を吸収して回復します。
#~  　　（例3）アクターの属性有効度100%、敵の属性ブースター20%、属性吸収120%の場合
#~  　　　　(1.0 * 1.2) - 1.2 = 0となり　ダメージは無効化されます
#~  
#~ 　　●攻撃属性
#~  　　スキルのメモ欄に｢<攻撃属性 3,4,5>｣と書いて設定します。
#~  　　例の場合、ID3、ID4、ID5の属性が攻撃属性に追加されます。
#~  　　ダメージ判定はスキルが持つ属性の中で最大のものが選択されます。
#~  
#~ 　　●攻撃属性追加
#~  　　装備品のメモ欄に「<攻撃属性スキル反映>」と書いて設定します。
#~  　　設定された武器を装備中、アクターのスキル攻撃に武器の属性が反映されるようになります。
#~  
#~ 　　●タイプを指定したステート発動率強化
#~ 　　　○割合強化
#~  　　　アクター、職業、武器、防具、ステートのメモ欄に『<ステート割合強化タイプ 1-30,2-10>』
#~  　　　と書いて設定します。
#~  　　　例の場合、スキルタイプID1、2のスキルはステート付与率がそれぞれ30%、10%上昇します
#~  　　　元の成功率が50%で30%上昇の設定をすれば成功率は『50*1.3 = 65%』となります
#~  
#~ 　　　○固定強化
#~  　　　アクター、職業、武器、防具、ステートのメモ欄に『<ステート固定強化タイプ 3-10,4-15>』
#~  　　　と書いて設定します。
#~  　　　例の場合、スキルタイプID3、4スキルはステート付与率がそれぞれ10%、15%上昇します
#~  　　　元の成功率が50%で10%上昇の設定をすれば成功率は『50+10 = 60%』となります。
#~  
#~ 　　　※仕様
#~  　　　割合強化と固定強化を設定した場合　（例）割合強化30%、固定強化20%の場合
#~  　　　元の成功率が50%とすると 『50 * 1.3 + 20 = 85%』となります。
#~  
#~ 　　●ステートIDを指定した発動率強化
#~ 　　　○割合強化
#~  　　　アクター、職業、武器、防具、ステートのメモ欄に『<ステート指定割合強化 1-30,2-10>』
#~  　　　と書いて設定します。
#~  　　　例の場合、ステートID1,2を与えるスキルの成功率がそれぞれ30%、20%割合で増加します
#~  
#~ 　　　○固定強化
#~  　　　アクター、職業、武器、防具、ステートのメモ欄に『<ステート指定固定強化 3-10,4-40>』
#~  　　　と書いて設定します。
#~  　　　例の場合、ステートID1,2を与えるスキルの成功率がそれぞれ10%、40%固定値で増加します
#~  
#~ 　　●スキルタイプブースター
#~  　　アクター、職業、武器、防具、ステートのメモ欄に『<スキルタイプ強化 1-20,2-5>』と書いて設定します。
#~  　　例の場合、スキルタイプID1,2のスキルの威力がそれぞれ20%、5%増加します。


#~ 　◎アクター、パーティー能力関連
#~ 　　●自動発動能力
#~ 　　　○戦闘開始時
#~ 　　　　アクター、職業、装備品、ステートのメモ欄に「<戦闘開始時発動 4,40%>」と書いて設定します。
#~ 　　　　例の場合、設定されたアクターは戦闘開始時40%の確率でID4のスキルを使用します。
#~ 　　　　この時スキルのコストは無視されます。複数の設定があればそのすべてを判定します。

#~ 　　　○ターン開始時
#~ 　　　　アクター、職業、装備品、ステートのメモ欄に「<ターン開始時発動 5,80%>」と書いて設定します。
#~ 　　　　例の場合、設定されたアクターはターン開始時80%の確率でID5のスキルを使用します。
#~ 　　　　この時スキルのコストは無視されます。複数の設定があればそのすべてを判定します。

#~ 　　　○ターン終了時
#~ 　　　　アクター、職業、装備品、ステートのメモ欄に「<ターン開始時発動 6,20%>」と書いて設定します。
#~ 　　　　例の場合、設定されたアクターはターン終了時20%の確率でID6のスキルを使用します。
#~ 　　　　この時スキルのコストは無視されます。複数の設定があればそのすべてを判定します。

#~ 　　●自爆耐性
#~ 　　　アクター、職業、装備品、ステートのメモ欄に「<自爆耐性>」と書いて設定します。
#~ 　　　設定されたアクターは｢<自爆スキル>｣による効果で戦闘不能にならずHP1で踏みとどまります。

#~ 　　●トリガーステート
#~ 　　　アクター、職業、装備品、ステートのメモ欄に「<トリガーステート H,0,40%,3>」と書いて設定します。
#~ 　　　例の場合、設定されたアクターはHPが40%未満になるとステートID3が付与されます。
#~ 　　　第1項目は H→HP　M→MP　T→TP　から選択できます。
#~ 　　　トリガーは0か3で指定　0→指定値未満で付与　1→指定値以上で付与
